<script>
document.addEventListener("DOMContentLoaded", function () {
    function updateNotifications() {
        fetch('../php/check_notifications.php')
            .then(response => response.json())
            .then(data => {
                document.getElementById("general-count").textContent = data.general;
                document.getElementById("gang-count").textContent = data.gang;
                document.getElementById("open-gang-count").textContent = data.openGang;

                // ซ่อน Badge ถ้าไม่มีแจ้งเตือน
                document.getElementById("general-count").style.display = data.general > 0 ? 'inline' : 'none';
                document.getElementById("gang-count").style.display = data.gang > 0 ? 'inline' : 'none';
                document.getElementById("open-gang-count").style.display = data.openGang > 0 ? 'inline' : 'none';
            })
            .catch(error => console.error("Error fetching notifications:", error));
    }

    // อัปเดตแจ้งเตือนทุก 5 วินาที
    updateNotifications();
    setInterval(updateNotifications, 5000);
});
</script>


